/**
 * Since most chips we work on are single core, spinlocks just disable scheduler and in some cases disable interrupts
 */

typedef struct {
	uint8_t dummy;
} spinlock_t;

#define spin_lock(lock) vTaskSuspendAll()
#define spin_unlock(lock) vTaskResumeAll()
